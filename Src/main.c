/* StartDefaultTask function */
void StartDefaultTask(void const * argument)
{

  /* USER CODE BEGIN 5 */
	scramble_init();

	/* Infinite loop */
	for(;;)
	{
		scramble_udp_msg tmp_msg;
		uint8_t flag = 0x00;

		__disable_irq();
		if(udp_updated){
			memcpy( &tmp_msg, (void *)&udp_msg, SIZE_UDP_MSG);
			flag = 1;
			udp_updated = 0;
		}
		__enable_irq();

		if(flag){
			float motor_rpm[4]={0};

			/*
			const float w[4][3] = {
					{-0.5, 0.86603,     WHEEL_DISTANCE},
					{-0.70711,-0.70711, WHEEL_DISTANCE},
					{ 0.70711,-0.70711, WHEEL_DISTANCE},
					{ 0.5, 0.86603,     WHEEL_DISTANCE}
			};
			*/
			
			const float w[4][3] = {
					{-0.5, 0.6,     WHEEL_DISTANCE},
					{-0.70711,-0.70711, WHEEL_DISTANCE},
					{ 0.70711,-0.70711, WHEEL_DISTANCE},
					{ 0.5, 0.6,     WHEEL_DISTANCE}
			};
			
			float v[3] = {tmp_msg.vx, tmp_msg.vy, tmp_msg.omega};

			for(int m=0;m<4;m++){
				for(int i=0;i<3;i++){
					motor_rpm[m] += w[m][i] * v[i];
				}
			}

			for(int m=0;m<4;m++){
				motor_rpm[m] *= GEAR_RATIO / (WHEEL_DIAMETER * 2 * M_PI) * 60;
			}

			uint8_t kick_flag = (tmp_msg.dribble_kick[0]>>4) & 0x01;
			uint8_t is_chip = (tmp_msg.dribble_kick[0]>>3) & 0x01;
			uint8_t kick_power = tmp_msg.dribble_kick[1] & 0x0f;

			//if the ball is held
			if( kick_flag && HAL_GPIO_ReadPin(BALL_GPIO_Port, BALL_Pin) == GPIO_PIN_SET ){
				//if the kicker is charged
				if( HAL_GPIO_ReadPin(KICK_READY_GPIO_Port, KICK_READY_Pin) == GPIO_PIN_SET ){
					set_kick(kick_flag, is_chip, kick_power);
				}
			}

			uint8_t dribble_power = (tmp_msg.dribble_kick[1] >> 4) & 0x0f;
			set_dribble(dribble_power*7);

			for(int i=0;i<4;i++){
				set_motor(i, motor_rpm[i]);
			}
			//printf("%6d %6d %6d %6d\r\n", (int)motor_rpm[0], (int)motor_rpm[1], (int)motor_rpm[2], (int)motor_rpm[3]);
		}
		osDelay(1);
	}
  /* USER CODE END 5 */ 
}

